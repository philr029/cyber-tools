export function getCategoryByRoute(route) {
  return TOOL_CATEGORIES.find((category) => category.route === route) ?? null;
}

export function getAllTools() {
  return TOOL_CATEGORIES.flatMap((category) =>
    category.tools.map((tool) => ({
      ...tool,
      categoryId: category.id,
      categoryRoute: category.route,
      categoryLabel: category.navLabel,
    })),
  );
}

export function getToolById(category, toolId) {
  return category.tools.find((tool) => tool.id === toolId) ?? null;
}

export function getDashboardStats() {
  const allTools = getAllTools();
  const checksAutomated = allTools.reduce((sum, tool) => sum + tool.checks.length, 0);

  return {
    toolsAvailable: allTools.length,
    checksAutomated,
    securityAreasCovered: 6,
    microsoft365Workflows: microsoft365Tools.length,
  };
}
